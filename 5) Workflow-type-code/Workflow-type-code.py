#Hay diferentes formas de escribir (y correr) codigo.

#IDE: Integrated Development Environment: Es un entorno de desarrollo integrado que 
# proporciona herramientas para escribir, depurar y ejecutar código de manera eficiente. 
# Ejemplos: PyCharm, VSCode, Jupyter Notebook.

#The Interpreter: Es un programa que ejecuta código línea por línea.
# Python es un lenguaje interpretado, lo que significa que el código se ejecuta directamente
# debe ser instalado separadamente en la computadora. VS code y otros 
# IDEs pueden usar el intérprete de Python para ejecutar el código.

#Script: Es un archivo de texto que contiene código fuente. Puede ser ejecutado/editado 
# por un IDE. Python scripts tienen la extensión .py

#Notebook: Aka jupyter notebook: También conocidos como Jupyter Notebooks, son archivos que pueden 
# contener código y texto en diferentes bloques llamados "celdas". 
# Se pueden editar y sus partes de código se pueden ejecutar en un IDE. Los Jupyter Notebooks 
# siempre tienen la extensión .ipynb. Se pueden exportar a otros formatos, como documentos de Word, 
# páginas HTML, archivos PDF e incluso diapositivas.

# Es un entorno interactivo que permite combinar código, texto, visualizaciones y otros elementos en 
# un solo documento. Es ideal para análisis de datos, aprendizaje automático y documentación.

#La terminal: Esta es la interfaz de texto que se usa para enviar instrucciones al sistema operativo 
# de la computadora. Normalmente se usa para instalar nuevos paquetes, por ejemplo, 
# con comandos como pip install nombre_del_paquete. Aunque la computadora también viene 
# con una aplicación de terminal independiente, puedes abrir una terminal en VS Code haciendo 
# clic en Terminal > Nueva terminal en la parte superior de la ventana de VS Code.

#markdown: Es un lenguaje de marcado ligero que se utiliza para formatear texto de manera sencilla.
# Se usa comúnmente en archivos README, documentación y en celdas de texto en Jupyter Notebooks.
# Permite agregar encabezados, listas, enlaces, imágenes y otros elementos de formato sin necesidad
# de utilizar etiquetas HTML complejas.

#Este es un lenguaje ligero que convierte comandos de texto simples 
# en documentos de aspecto profesional. Es ampliamente utilizado por programadores. 
# También se utiliza para las celdas de texto en Jupyter Notebooks. Cuando no están en un notebook, 
# los archivos que contienen Markdown siempre tienen la extensión .md. Con las extensiones 
# de Markdown de Visual Studio Code instaladas, puede hacer clic derecho dentro de un archivo 
# Markdown en VS Code y luego seleccionar "Vista previa de Markdown mejorada" para ver cómo 
# se verá el documento renderizado. La diferencia entre el código HTML y el mismo sitio web 
# visto en un navegador como Chrome es una buena analogía para la diferencia entre lo que se ve 
# en un archivo .md y lo que se ve en la vista previa de ese archivo Markdown.

#quarto markdown: Es una extensión de Markdown que permite integrar código ejecutable
# dentro de documentos de texto. Es especialmente útil para crear informes reproducibles
# y presentaciones que combinan análisis de datos con explicaciones narrativas.
# Los archivos Quarto Markdown tienen la extensión .qmd y pueden ser procesados
# para generar documentos en varios formatos, como HTML, PDF y Word.

